public class Bai8_ToanTuGan {
    public static void main(String[] args) {
        //Gán cộng
        int x = 8;
        x+=6; //x=x+6
        System.out.println(x);

        //Gán trừ
        int x2 = 8;
        x2-=3; //x2=x2-3;
        System.out.println(x2);

        //Gán nhân
        int x3 = 3;
        x3*=3; //x3=x3*3;
        System.out.println(x3);

        //Gán chia lấy dư
        int x4 = 10;
        x4%=3; //x4 = x4%3
        System.out.println(x4);

        int a =7;
        int b =9;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
