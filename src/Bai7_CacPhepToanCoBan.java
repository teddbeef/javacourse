public class Bai7_CacPhepToanCoBan {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        //phép cộng
        System.out.println("a + b = " + (a+b));

        //gán biến
        int kq = a + b;
        System.out.println("a + b = " +kq);
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a % b = " + (a%b));
        System.out.println("a / b = " + ((double)a/b));
    }
}
